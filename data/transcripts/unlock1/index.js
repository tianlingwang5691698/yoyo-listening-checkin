function buildTrack(trackId, contentId, durationSec, rawLines) {
  const minLineMs = 1800;
  const totalDurationMs = durationSec * 1000;
  const normalized = rawLines
    .map((line) => String(line || '').replace(/\s+/g, ' ').trim())
    .filter(Boolean);
  const weights = normalized.map((line) => Math.max(line.replace(/\s+/g, '').length, 6));
  const totalWeight = weights.reduce((sum, value) => sum + value, 0);
  const baseDuration = Math.max(totalDurationMs - (normalized.length * 200), normalized.length * minLineMs);
  let cursor = 0;

  const lines = normalized.map((text, index) => {
    const isLast = index === normalized.length - 1;
    const weightedMs = Math.max(minLineMs, Math.round((weights[index] / totalWeight) * baseDuration));
    const endMs = isLast ? totalDurationMs : Math.min(totalDurationMs, cursor + weightedMs);
    const line = {
      lineId: `${contentId}-${index + 1}`,
      text,
      startMs: cursor,
      endMs: Math.max(cursor + minLineMs, endMs)
    };
    cursor = line.endMs;
    return line;
  });

  if (lines.length) {
    lines[lines.length - 1].endMs = totalDurationMs;
  }

  return {
    trackId,
    contentId,
    mediaType: 'audio',
    lines
  };
}

